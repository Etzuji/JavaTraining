public class BooleanDemo1 {

    public static void main(String[] args) {

        boolean dasWetterIstSchoen = false;
        boolean lampeIstEingeschaltet = false;
        boolean druckerIstBereit = true;
        boolean kannDuplexdruck = false;

        System.out.println("Ist das Wetter schön? " + dasWetterIstSchoen);
        System.out.println("Ist das Licht an? " + lampeIstEingeschaltet);
        System.out.println("Ist der Drucker bereit? " + druckerIstBereit);
        System.out.println("Ist Drucker duplexfähig? " + kannDuplexdruck);

    }

}